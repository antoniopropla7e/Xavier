// Variable de entrada: la temperatura actual en grados Celsius
const temperatura = 28; // Prueba con diferentes valores (e.g., 28, 15, 25)

// Estructura Condicional if-else
if (temperatura > 25) {
    // Bloque que se ejecuta si la condición es VERDADERA (temperatura es mayor a 25)
    console.log(`¡Hace ${temperatura}°C! ☀️ Es un día perfecto para salir a pasear. ¡A disfrutar!`);
} else {
    // Bloque que se ejecuta si la condición es FALSA (temperatura es 25 o menor)
    console.log(`Hace ${temperatura}°C. ☁️ Quizás es mejor quedarse en casa, tomar un café y programar. 😉`);
}